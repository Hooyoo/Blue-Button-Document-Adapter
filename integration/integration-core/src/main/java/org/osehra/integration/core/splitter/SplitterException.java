package org.osehra.integration.core.splitter;

public class SplitterException extends Exception {
	private static final long serialVersionUID = -6319976583889211398L;

	public SplitterException() {
		super();
	}

	public SplitterException(final String msg) {
		super(msg);
	}

	public SplitterException(final String message, final Throwable cause) {
		super(message, cause);
	}

	public SplitterException(final Throwable cause) {
		super(cause);
	}

}
